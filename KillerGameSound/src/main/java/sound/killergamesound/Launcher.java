/**
 * The Launcher class is responsible for launching the KillerGameSound application.
 * It simply calls the main method of the Main class to start the application.
 */
package sound.killergamesound;

import sound.killergamesound.Main.Main;

public class Launcher {
    /**
     * The main method of the Launcher class.
     * It calls the main method of the Main class to start the application.
     * @param args The command-line arguments.
     */
    public static void main(String[] args) {
        Main.main(args);
    }
}
